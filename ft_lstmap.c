#include "libft.h"

t_list	*ft_lstmap(t_list *list, t_list *(*f)(t_list *))
{
	t_list	*list_;

	if (list && f)
	{
		list_ = f(list);
		if (list_ && list->next)
			list_->next = ft_lstmap(list->next, f);
		return (list_);
	}
	return (NULL);
}
