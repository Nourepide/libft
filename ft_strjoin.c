#include "libft.h"

char	*ft_strjoin(char const *str1, char const *str2)
{
	char	*result;

	if (!str1 || !str2)
		return (NULL);
	if (!(result = ft_strnew(ft_strlen(str1) + ft_strlen(str2))))
		return (NULL);
	ft_strcat(result, str1);
	ft_strcat(result, str2);
	return (result);
}
