#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t size)
{
	if (!dst && !src)
		return (NULL);
	if (dst < src)
		ft_memcpy(dst, src, size);
	else
		ft_memrcpy(dst, src, size);
	return (dst);
}
