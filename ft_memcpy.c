#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t size)
{
	unsigned char	*dst_;
	unsigned char	*src_;

	dst_ = (unsigned char *)dst;
	src_ = (unsigned char *)src;
	if (dst || src)
		while (size--)
			*dst_++ = *src_++;
	return (dst);
}
