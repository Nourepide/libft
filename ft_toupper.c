#include "libft.h"

int		ft_toupper(int chr)
{
	if ((chr >= ASCII_LOWERCASE_A && chr <= ASCII_LOWERCASE_Z))
		return (chr - (ASCII_LOWERCASE_A - ASCII_UPPERCASE_A));
	else
		return (chr);
}
