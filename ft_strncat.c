#include "libft.h"

char	*ft_strncat(char *str1, const char *str2, size_t size)
{
	size_t	str_size;

	str_size = ft_strlen(str1);
	size = ft_strnlen(str2, size);
	*(str1 + str_size + size) = ASCII_NULL;
	ft_strncpy(str1 + str_size, str2, size);
	return (str1);
}
